    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        // TODO add your handling code here:
        String str_user = txt_usuario.getText();
        String str_pass = String.valueOf(txt_password.getPassword());
        
        System.out.println("str_user->"+str_user);
        System.out.println("str_pass->"+str_pass);
        
        try{
            
            Connection conn = Conexion.getConnection();
            
            String consulta = "SELECT * FROM "+Conexion.tblusuarios+" "
                    + "WHERE "
                    + "us_usuario=? AND us_pass=?";
            

            
            //preparar:
            PreparedStatement query = conn.prepareStatement(consulta);
            query.setString(1, str_user);
            query.setString(2, str_pass);
            
            
            ResultSet rs = query.executeQuery();
            
            //resultados
            if( rs.next() ){
                
                String nombre = rs.getString("us_nombre");
                
                JOptionPane.showMessageDialog(null,"Bienvenido: "+nombre);
                
               
                //ventana nueva
                Index vt = new Index();
                
                this.dispose();
                
                vt.setVisible(true);
                
            }else{
                 JOptionPane.showMessageDialog(null,"Usuario no existe ");
            }//else
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "ERROR Login->"+e);
        }//catch
    }//GEN-LAST:event_btn_aceptarActionPerformed

